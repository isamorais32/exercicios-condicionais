let dia = 5;
let resp;


switch(dia){
    case 1:
        resp = "domingo"
        break;
    case 2:
        resp = "segunda"
        break;
    case 3:
        resp = "terca"
        break;
    case 4:
        resp = "quarta"
        break;
    case 5:
        resp = "quinta"
        break;
    case 6:
        resp = "sexta"
        break;
    case 7:
        resp = "sabado"
        break;
    default:
        resp = "dia invalido"
}
console.log(resp);